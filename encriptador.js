function encriptar(texto) {
    for (let letra of texto) {
        if (letra == "a") {
            letra = "ai";
        } else if (letra == "e") {
            letra = "enter";
        } else if (letra == "i") {
            letra = "imes";
        } else if (letra == "o") {
            letra = "ober";
        } else if (letra == "u") {
            letra = "ufat";
        }
    }
}

